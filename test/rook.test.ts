import test from 'ava'
import { computeMovesAsStringBoard } from './testUtils.js'

test('rook captures and blocked squares', t => {
	const board = [
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", "P", "P", "P", " ", " "],
		[" ", " ", " ", " ", "_r", " ", " ", " "],
		[" ", " ", " ", "p", " ", "p", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "]
	]

	const actual = computeMovesAsStringBoard(board)

	const expected = [
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", "P", "x", "P", " ", " "],
		["x", "x", "x", "x", "r", "x", "x", "x"],
		[" ", " ", " ", "p", "x", "p", " ", " "],
		[" ", " ", " ", " ", "x", " ", " ", " "],
		[" ", " ", " ", " ", "x", " ", " ", " "]
	]

	t.deepEqual(actual, expected)
})

