import test from 'ava';
import { computeMovesAsStringBoard } from './testUtils.js';

test('king basic', t => {
	const board = [
		["r", "n", "b", "q", "_k", "b", "n", "r"],
		["p", "p", "p", "p", " ", "p", "p", "p"],
		[" ", " ", " ", " ", "p", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		["P", "P", "P", "P", "P", "P", "P", "P"],
		["R", "N", "B", "Q", "K", "B", "N", "R"]
	]

	const actual = computeMovesAsStringBoard(
		board
	)

	const expected = [
		["r", "n", "b", "q", "k", "b", "n", "r"],
		["p", "p", "p", "p", "x", "p", "p", "p"],
		[" ", " ", " ", " ", "p", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		[" ", " ", " ", " ", " ", " ", " ", " "],
		["P", "P", "P", "P", "P", "P", "P", "P"],
		["R", "N", "B", "Q", "K", "B", "N", "R"]
	]

	t.deepEqual(actual, expected)
})

